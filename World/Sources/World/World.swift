        Self.nativeLanguageNames[code]
    }

    /// 获取某个地区的原名（以其官方语言书写的官方名称）。
    ///
    /// - Parameter code: ISO 3166-1 Alpha 2 国家和辖区代码
    public static func nativeName(ofRegion code: String) -> String? {
        Self.nativeRegionNames[code]
    }

    /// 通过语言和地区代码组合构造有效的 `Locale`。
    ///
    /// 此函数保证输入的地区不变，且保证构造的 `Locale` 使用当地官方语言之一。将依次尝试以下组合：
    ///
    /// 1. 如果输入的语言是输入地区的官方语言（或之一），直接构造 `Locale`。
    /// 2. 如果 App 支持的语言之一是输入地区的官方语言，使用此组合构造 `Locale`。
    /// 3. 如果用户偏好的语言列表中存在地区符合输入地区的语言、且此语言是当地官方语言（或之一），构造此语言和输入地区组合的 `Locale`。
    /// 4. 使用输入地区的首选官方语言构造组合的 `Locale`。
    ///
    /// 所有上述组合在最终返回前仍会经过系统调整，但这种调整使用的内部规则不得而知。
    ///
    /// - Parameters:
    ///   - language: ISO 639-1 语言代码
    ///   - region: ISO 3166-1 Alpha 2 国家和辖区代码
    public static func preferredLocale(language: String, region: String) -> Locale {
        if Self.isLanguage(language, spokenIn: region) {
            return Locale(identifier: "\(language)-\(region)")
        }

        for preference in Bundle.main.preferredLocalizations {
            guard let preferredLanguage = Locale(identifier: preference).languageCode else {
                continue
            }
            if Self.isLanguage(preference, spokenIn: region) {
                return Locale(identifier: "\(preferredLanguage)-\(region)")
            }
        }

        for preference in Locale.preferredLanguages {
            let locale = Locale(identifier: preference)
            guard locale.regionCode == region, let preferredLanguage = locale.languageCode else {
                continue
            }
            if Self.isLanguage(preference, spokenIn: region) {
                return Locale(identifier: "\(preferredLanguage)-\(region)")
            }
        }

        guard let preferred = Self.languages[region]?.first else {
            let locale = Locale(identifier: Bundle.main.preferredLocalizations.first!)
            return Locale(identifier: "\(locale.languageCode!)-\(region)")
        }
        return Locale(identifier: "\(preferred)-\(region)")
    }
