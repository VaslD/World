    /// 检查某地区是否使用某种法定货币，并额外写回是否为首选货币。
    ///
    /// ```swift
    /// var isPreferred = false
    /// let isUsed = World.isCurrency("USN", usedIn: "US", preferred: &isPreferred)
    ///
    /// print(isUsed)       // true
    /// print(isPreferred)  // false
    /// ```
    ///
    /// 某些货币，例如 USN (US Dollar: Next Day) 和 USS (US Dollar: Same Day)
    /// 是标准定义的货币并且在特殊金融场景下需要使用，但大多数场景不会以此货币结算。
    ///
    /// 因此，建议将此函数仅作为校验使用，并谨慎处理非首选货币的情况。
    ///
    /// > Important:
    /// 此函数不检查货币和地区代码有效性，也不返回可选类型。输入无效的代码将因为找不到对应匹配而返回 `false`。请单独使用
    /// ``isValid(currency:common:)`` 和 ``isValid(region:)`` 检查代码是否在 ISO 标准中定义。
    ///
    /// - Parameters:
    ///   - code: ISO 4217 货币代码
    ///   - region: ISO 3166-1 Alpha 2 国家和辖区代码
    ///   - preferred: 是否为首选货币
    public static func isCurrency(_ code: String, usedIn region: String,
                                  preferred: UnsafeMutablePointer<Bool>? = nil) -> Bool {
        let isPreferred = code == Self.currencies[region]?.first
        if isPreferred {
            preferred?.pointee = true
            return true
        }

        preferred?.pointee = false
        return Self.currencies[region]?.dropFirst().contains(code) ?? false
    }

    /// 获取某个地区的 Emoji (Unicode 6.0) 旗帜。
    ///
    /// ```swift
    /// print(World.flag(for: "CN"))  // 🇨🇳
    /// ```
    ///
    /// > Note:
    /// 中国大陆购买的 Apple 设备可能无法显示香港、澳门、台湾的区旗，但相关的 Unicode 字符和 ISO 地区代码都是有效的。
    ///
    /// > Important:
    /// 此函数不检查地区代码有效性，也不返回可选类型。输入无效的代码将返回 🌐 标志。请单独使用
    /// ``isValid(region:)`` 检查地区是否在 ISO 标准中定义。
    ///
    /// - Parameter region: ISO 3166-1 Alpha 2 国家和辖区代码
    public static func flag(for region: String) -> String {
        // 此函数涉及 String 和 Character 在 Unicode 和 Emoji 标准下的工作方式，请勿修改。
        // https://en.wikipedia.org/wiki/Regional_indicator_symbol

        var flag = String()
        for letter in region {
            guard let character = Self.regionalIndicatorSymbols[letter] else {
                return "🌐"
            }
            flag.append(character)
        }
        return flag
    }

