    public static func isValid(localization: Locale) -> Bool? {
        guard let language = localization.languageCode, let region = localization.regionCode else {
            return nil
        }
        return Self.isLanguage(language, spokenIn: region)
    }

    public static func isValid(currency: String, common: UnsafeMutablePointer<Bool>? = nil) -> Bool {
        if Locale.commonISOCurrencyCodes.contains(currency) {
            common?.pointee = true
            return true
        }

        common?.pointee = false
        return Locale.isoCurrencyCodes.contains(currency)
    }

